
a = 6
b = 3

suma = a + b
resta = a - b
multi = a * b # balatro

division = a / b # Devuelve siempre un numero decimal
divisionEntera = a // b # Devuelve un numero entero, quitando la parte decimal
resto = a % b 
# 6/2 = 3, resto = 0
# 6/4 = 1, resto = 2

elevado = a ** 2 # el ^ sirve para otra cosa

print(suma, resta, multi)
print(division, divisionEntera, resto)
print(elevado)

